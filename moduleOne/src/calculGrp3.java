public class calculGrp3 {
     public static double addition(double n1, double n2) {
        double resultat = n1 + n2;
        return resultat;
    }
    public static double pourcentage(double partie, double total) {
        double resultatpourcentage = (partie / 100) * total;
        return resultatpourcentage;
    }
    public static double cosinus(double chiffre) {
        double rest = Math.cos(chiffre);
        return rest;
    }
}
